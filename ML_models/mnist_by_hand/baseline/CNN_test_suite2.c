float maxabs(k2c_tensor_wa *tensor1, k2c_tensor_wa *tensor2) {

    float x = 0;

    float y = 0;

    for(size_t i=0; i<tensor1->numel; i++) {

        y = fabsf(tensor1->array[i]-tensor2->array[i]);
        if (y>x) {
            x=y;
        }
    }
    return x;
}

